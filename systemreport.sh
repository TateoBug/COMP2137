#!/bin/bash
Username=$USERNAME
Date=`(date +%c)`
Hostname=$HOSTNAME
source /etc/os-release
OS=$PRETTY_NAME
Uptime=`(uptime -p)`
CPU=`(lscpu | grep 'Model name:' | awk -F':' '{print $2}' | xargs)`
Speed=`(cat /proc/cpuinfo | grep 'MHz' | uniq)`



cat << EOF

System Report generated by, $Username, $Date

System Information
------------------------------
Hostname: $Hostname
OS: $OS
Uptime: $Uptime


Hardware Information
------------------------------
CPU: $CPU
Speed: $Speed

EOF
