#!/bin/bash
Username=$USERNAME
Date=`(date +%c)`
Hostname=$HOSTNAME
source /etc/os-release
OS=$PRETTY_NAME
Uptime
cat << EOF

System Report generated by, $Username, $Date

System Information
------------------------------
Hostname: $Hostname
OS:$OS
Uptime:$(uptime -p)


Hardware Information
------------------------------
CPU:$(lscpu -C)


EOF
